     * Display a listing of the resource.
     *
     * @return \Illuminate\Http\Response
     */
    public function index()
    {
        $profile = auth()->user()->profile;
        if(isset($profile)){
            return redirect(route('profile.show', ['profile' => $profile->id]));
        }else{
            return redirect(route("profile.create"));
        }
    }

